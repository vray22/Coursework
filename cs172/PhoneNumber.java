public class PhoneNumber// Class of Random  Phone Number 
{
public static void main(String[] args) 
 { 
         System.out.print(" Random phone number is ");// Prints out Random phone number is 
         
         int num1 = 3;// initialize num1 as 3
         System.out.printf("%03d",num1);// prints num1 out with zeros in front of it 
         System.out.print("-");// prints out a dash 
         
         int num2 = 2;// initialize num2 as 2 
         System.out.printf("%03d",num2);// prints num2 out with zeros in front of it 
         
         int num3 = 345;// initialize num3 as 345 
         System.out.print("-" + num3);// prints out a dash followed by num3 
         
   }
  }
          
